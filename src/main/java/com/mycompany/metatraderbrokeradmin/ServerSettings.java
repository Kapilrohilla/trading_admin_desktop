    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        OkHttpClient client = new OkHttpClient();
        JSONObject payloadJSON = new JSONObject();
        String ip = jTextField3.getText();
        String end_of_day = jTextField1.getText();
        String holidays = jTextField1.getText();

        try {

            payloadJSON.put("ip_address", ip);
            payloadJSON.put("end_of_day", end_of_day);
            payloadJSON.put("holidays", holidays);
            payloadJSON.put("adminId", BrokerAdmin.adminId);
            String apiUrl = APIs.UPDATE_ADMIN;
            MediaType JSON = MediaType.parse("application/json; charset=utf-8");
            RequestBody body = RequestBody.create(JSON, payloadJSON.toString());

            Request request = new Request.Builder()
                    .url(apiUrl)
                    .post(body)
                    //                    .addHeader("Authorization", BrokerAdmin.adminId)
                    .build();

            Response response = client.newCall(request).execute();
            if (response.isSuccessful()) {
                JOptionPane.showMessageDialog(ServerSettings.this, "SuccessFully Updated");
                dispose();
                System.out.println("Admin update successful");
                System.out.println("Response: " + response.body().string());
            } else {
                System.out.println("admin update Failed" + response.message());
            }
        } catch (JSONException | IOException ex) {
            System.out.println(ex.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

