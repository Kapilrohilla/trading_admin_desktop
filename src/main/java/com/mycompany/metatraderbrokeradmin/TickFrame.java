    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
                    String bid = jTextField3.getText();
                    String ask = jTextField4.getText();
                    String last = jTextField5.getText();

                    JSONObject payload = new JSONObject();
                    payload.put("tickId", t.getId());
                    payload.put("bid", bid);
                    payload.put("ask", ask);
                    payload.put("ltp", last);

                    OkHttpClient client = new OkHttpClient();
                    String apiUrl = APIs.UPDATE_TICK;
                    MediaType JSON = MediaType.parse("application/json; charset=utf-8");
                    RequestBody body = RequestBody.create(JSON, payload.toString());

                    Request request = new Request.Builder()
                            .url(apiUrl)
                            .post(body)
                            .build();

                    Response response = client.newCall(request).execute();
                    if (response.isSuccessful()) {
                        JOptionPane.showMessageDialog(TickFrame.this,"SuccessFully Updated"); 
                        dispose();
                        System.out.println("Signup successful");
                        System.out.println("Response: " + response.body().string());
                    } else {
                        System.out.println("Signup Failed" + response.message());
                    }
                } catch (IOException | JSONException ex) {
                    System.out.println("Error occurred while login");
                }
    }//GEN-LAST:event_jButton1ActionPerformed

